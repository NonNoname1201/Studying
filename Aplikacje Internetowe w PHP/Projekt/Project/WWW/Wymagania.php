

Wymagania dotyczące projektu końcowego
1. Projekt końcowy wymagany na zaliczenie powinien stanowić w pełni funkcjonalną
aplikację webową (tematyka dowolna) zaimplementowaną w języku PHP z
wykorzystaniem szablonu obiektowego. Na maksymalną liczbę punktów wskazane
jest zastosowanie wzorca MVC i/lub wybranego frameworka (np. Laravel).

2. Szata graficzna projektu może zostać przygotowana w oparciu o darmowy szablon,
który zostanie przystosowany do specyfiki projektu. Obowiązuje zgodność ze
standardem HTML5/CSS3. Wynikowy kod HTML oraz arkusze CSS mają być zgodne
ze standardem W3C – walidacja nie powinna wykazywać błędów. Można skorzystać
z projektu tworzonego w poprzednim semestrze.

3. Aplikacja powinna współpracować z bazą danych (np. MySQL) z wykorzystaniem
interfejsu mysqli/pdo (lub warstwy ORM stosowanej w konkretnym frameworku) i
udostępniać wybrane funkcjonalności (np. zapis nowych rekordów do bazy, edycję
istniejących) tylko dla zalogowanych użytkowników. Praca z bazą danych ma
oferować funkcjonalności typowej aplikacji CRUD (Create, Read, Update, Delete).

4. Hasła w bazie muszą być haszowane a funkcje serwisu dostępne dla zalogowanych
użytkowników powinny być zabezpieczone przed nieuprawnionym dostępem.

5. Wszystkie formularze powinny być walidowane po stronie klienta (w tym celu
wystarczy zastosować odpowiednie typy input HTML5) i po stronie serwera (należy
wykorzystać odpowiednie filtry i wyrażenia regularne).

6. Będzie oceniana jakość kodu i jego organizacja (paradygmat OOP).

7. Ostateczny termin oddania projektu - ostatnie laboratorium w semestrze.